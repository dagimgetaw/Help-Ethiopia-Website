const mongoose = require("mongoose");

const MessageSchema = new mongoose.Schema(
  {
    fullName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

const messageModel = mongoose.model("messages", MessageSchema);

module.exports = messageModel;
